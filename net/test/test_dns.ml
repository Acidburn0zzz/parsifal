let test_raw_message name content =
  print_endline (print_value (value_of_dns_message (parse_dns_message (input_of_string ~enrich:NeverEnrich name content))))

let test_message name content =
  print_endline (print_value (value_of_dns_message (parse_dns_message (input_of_string name content))))

let _ =
  try
    print_endline "RAW VERSION\n";
    test_raw_message "dns_query" dns_query;
    test_raw_message "dns_answer" dns_answer;

    print_newline ();
    print_endline "SMART VERSION\n";
    test_message "dns_query" dns_query;
    test_message "dns_answer" dns_answer;
  with
  | ParsingException (e, h) -> prerr_endline (string_of_exception e h); exit 1
  | e -> prerr_endline (Printexc.to_string e); exit 1
